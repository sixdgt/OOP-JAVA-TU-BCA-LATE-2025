package example;

public class FirstJavaProgramExample {
	public static void main(String args[]) {
		String title = "First Java Program";
		String operation = "Adding two whole number";
		String subTitle = "This is our first session";
		int num_one = 45;
		int num_two = 50;
		int sum = num_one + num_two;
		System.out.println("Title: " + title);
		System.out.println("Subtitle: " + subTitle);
		System.out.println("Operation: " + operation);
		System.out.println("Sum of two whole number is: " + sum);
	}
}
